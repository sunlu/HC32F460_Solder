/**
 * @brief  将 UTF-8 字符串的当前字符解码为 Unicode 码点
 * @param  ptr: 字符串指针的指针（解码后指针会向后移动对应的字节数）
 * @return uint32_t: 返回 Unicode 编码
 */
static uint32_t utf8_to_unicode(const char **ptr) {
    const uint8_t *p = (const uint8_t *)*ptr;
    uint32_t c = *p;

    if (c == 0) return 0;

    if (c < 0x80) {          // 单字节 ASCII
        *ptr += 1;
        return c;
    } else if ((c & 0xE0) == 0xC0) { // 2 字节字符
        c = ((p[0] & 0x1F) << 6) | (p[1] & 0x3F);
        *ptr += 2;
        return c;
    } else if ((c & 0xF0) == 0xE0) { // 3 字节字符（绝大多数常用汉字在这里）
        c = ((p[0] & 0x0F) << 12) | ((p[1] & 0x3F) << 6) | (p[2] & 0x3F);
        *ptr += 3;
        return c;
    } else if ((c & 0xF8) == 0xF0) { // 4 字节字符
        c = ((p[0] & 0x07) << 18) | ((p[1] & 0x3F) << 12) | ((p[2] & 0x3F) << 6) | (p[3] & 0x3F);
        *ptr += 4;
        return c;
    }
    *ptr += 1; // 错误编码保护
    return 0;
}

