bool get_pixel_from_glyph(const GFXglyph *g, const uint8_t *bitmap, uint8_t x, uint8_t y) {
    // 仅保留核心的位解包逻辑
    uint32_t bitIndex  = (uint32_t)y * g->width + x;
    uint32_t byteIndex = g->bitmapOffset + (bitIndex / 8);
    uint8_t  bitOffset = 7 - (bitIndex % 8);

    return (bitmap[byteIndex] >> bitOffset) & 0x01;
}